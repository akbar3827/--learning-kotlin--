package com.example.learn_kotlin.OOP


fun main() {
    val a = 2
    val x = 5

    a.unaryPlus() // +a
    a.unaryMinus() // -a
    a.inc() // a++
    a.dec() //a--
    a.plus(x) // a + x
    a.minus(x) // a - x
    a.times(x) // a * x
    a.rem(x) // a % x
    a.div(x) // a / x
    a.rangeTo(x) // a...x
}